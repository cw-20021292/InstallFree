/***********************************************************************************************************************
* Function Name: System_ini
* Description  :
***********************************************************************************************************************/
void main_global_p(void)
{
    System_ini();

    while(1)
    {
        R_WDT_Restart();
        Sync_100MS();

        if(u8FactoryTestMode > 0)
        {
            Test_Mode_Main();
        }
        else
        {
            Input();
            Output();
            Mode_Control();
            Display_Control();
            Error_Control();
        }
    }
}/* --- End of main() --- */

/***********************************************************************************************************************
* Function Name: System_ini
* Description  :
***********************************************************************************************************************/
void Sync_50MS(void)
{
    while(u8CountSync < 50)
    {
        ;
    }

    /*u8CountSync = CLEAR;*/
}

/***********************************************************************************************************************
* Function Name: System_ini
* Description  :
***********************************************************************************************************************/
void Sync_100MS(void)
{
    while(u8CountSync < 100)
    {
        ;
    }

    u8CountSync = CLEAR;
}

